#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(int ac, char **av)
{
	int j;

	if	(ac >= 2)
	{
		j = 0;
		while (av[ac - 1][j] != '\0')
			ft_putchar(av[ac - 1][j++]);
	}
	ft_putchar('\n');
	return (0);
}


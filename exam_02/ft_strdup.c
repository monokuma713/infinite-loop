#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char *dup;
	int i;

	i = 0;
	while (src[i] != '\0')
		i++;
	dup = (char *)malloc(sizeof(char) * i + 1);
	dup[i] = '\0';
	while (i >= 0)
	{
		dup[i] = src[i];
		i--;
	}
	return (dup);
}

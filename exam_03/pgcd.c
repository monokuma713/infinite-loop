#include <stdio.h>
#include <stdlib.h>

int		main(int ac, char **av)
{
	int a;
	int b;

	if (ac == 3)
	{
		a = atoi(av[1]);
		b = atoi(av[2]);
		while (a != b)
			(a > b) ? (a = a - b) : (b = b - a);
		printf("%d", a);
	}
	printf("\n");
	return (0);
}

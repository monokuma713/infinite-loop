#include <unistd.h>

void	ft_hidenp(char *a, char *b)
{
	while (*b)
		if (*a == *b++)
			a++;
	(*a == '\0') ? write(1, "1", 1) : write(1, "0", 1);
}

int		main(int ac, char **av)
{
	if (ac == 3)
		ft_hidenp(av[1], av[2]);
	write(1, "\n", 1);
	return (0);
}

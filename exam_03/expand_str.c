#include <unistd.h>

int		space(char a)
{
	return ((a == ' ' || a == '\t' || a == '\0') ? 1 : 0);
}

int		main(int ac, char **av)
{
	int i;

	if (ac == 2)
	{
		i = 0;
		while (space(av[1][i]) == 1 && av[1][i] != '\0')
			i++;
		while (av[1][i] != '\0')
		{
			if (space(av[1][i]) == 0)
				write(1, &av[1][i], 1);
			else if(space(av[1][i]) == 1)
			{
				if (space(av[1][i + 1]) == 0)
					write(1, "   ", 3);
			}
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
				

